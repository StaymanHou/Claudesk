    #[test]
    fn native_row_never_carries_content() {
        // The privacy invariant, stated as a test on STRUCTURED fields (not a bare
        // !contains substring check — that was WP2 MINOR #1's weakness). The
        // NativeSignal enum only carries counts/enums/bools, so there is no field
        // that COULD hold content; this test pins that no builder ever widens that.
        // We feed a keystroke of a "secret" LENGTH and assert only the count survives,
        // and that every meta VALUE is a number, bool, or a known-safe enum/handle —
        // never a free-form content string.
        let mut c = ctx();
        c.session_id = Some("cc-1".into());
        // The "content" would be 11 bytes of "SECRETKEYS!" — only the length 11 lands.
        let row = native_row(&NativeSignal::KeystrokeActivity { byte_count: 11 }, &c);
        let meta: serde_json::Value = serde_json::from_str(row.meta.as_ref().unwrap()).unwrap();

        // byte_count is present and numeric.
        assert_eq!(meta["byte_count"], json!(11));
        // Every meta value is a safe type: number, bool, or one of the known
        // attribution strings (workspace_id / surface). No unexpected content string.
        let allowed_string_keys = ["workspace_id", "surface"];
        for (k, v) in meta.as_object().unwrap() {
            match v {
                serde_json::Value::Number(_) | serde_json::Value::Bool(_) => {}
                serde_json::Value::String(_) => assert!(
                    allowed_string_keys.contains(&k.as_str()),
                    "meta key {k} is a string but not a known-safe attribution field"
                ),
                other => panic!("unexpected meta value type for {k}: {other:?}"),
            }
        }
        // And the row's own text columns never carry content: tool_name/agent_type
        // are None for a keystroke row; session_id/cwd are opaque handles/paths.
        assert_eq!(row.tool_name, None);
        assert_eq!(row.agent_type, None);
    }

