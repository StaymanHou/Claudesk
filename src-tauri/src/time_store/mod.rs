/// The `source` tag stamped on every row this module writes — the CC-hook event
/// stream. WP2.5 will write native-signal rows with a different tag into the same
/// table; the discriminator lets WP3/WP4 tell the two apart.
pub const SOURCE_CC_HOOK: &str = "cc-hook";

// WP2.5 native-signal STORE layer (built Phase 1) is FULLY WIRED as of Phase 4 — no
// `#[allow(dead_code)]` remains. Callers: focus handler (`record_focus_change`, Phase 2),
// `time_set_active_context` (`ActiveSurface`, Phases 2/4), `cc_input`
// (`record_keystroke_activity`, Phase 3), launch commands (`record_external_launch`,
// Phase 4). Every `NativeSignal` variant + `NativeLaunchTool` + all helpers are live.

/// The `source` tag stamped on every **Claudesk-native** signal row (M9 WP2.5): the
/// second event source, capturing what `claude-time` could only infer (window
/// focus/blur, real PTY keystroke activity, active surface, Claudesk-initiated
/// external launches). Written into the SAME `events` table as the CC-hook rows —
/// the `source` discriminator lets WP3's reclassifier read one stream and tell the
/// two sources apart. See [`NativeSignal`] for the event kinds.
pub const SOURCE_CLAUDESK_NATIVE: &str = "claudesk-native";

/// Epoch-ms "now" for **native-signal** rows. Unlike CC-hook rows (whose `ts` comes
/// from the hook-side `timestamp`), native signals are generated by Claudesk itself,
/// so they stamp their own send-time here — they NEVER hit [`event_to_row`]'s
/// epoch-0 fallback. Uses `SystemTime` (available in the app, unlike workflow
/// scripts). A pre-1970 clock (impossible in practice) clamps to 0.
pub fn now_ms() -> i64 {
    use std::time::{SystemTime, UNIX_EPOCH};
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|d| d.as_millis() as i64)
        .unwrap_or(0)
}

/// The Claudesk-native signal kinds WP2.5 captures. Each maps to a [`TimeRow`] via
/// [`native_row`], carrying `source = "claudesk-native"` and the signal's extras in
/// the `meta` JSON blob. **Privacy invariant (extends WP2): counts / timing /
/// attribution ONLY — never keystroke content, never a launched file path beyond the
/// attributed workspace.** The variants deliberately carry only enumerable/numeric
/// fields so there is no channel for content to leak.
#[derive(Debug, Clone, PartialEq, Eq)]
pub enum NativeSignal {
    /// The Claudesk main window gained focus. Attributed to the currently-active
    /// workspace/surface (from the frontend's `time_set_active_context`), if any.
    WindowFocus,
    /// The Claudesk main window lost focus. Paired with a later [`WindowFocus`] so
    /// WP3 can derive blur *duration*; `preceded_by_launch` lets WP3 resolve the
    /// "blur-but-working" case (operator popped an external tool then looked at it).
    WindowBlur { preceded_by_launch: bool },
    /// Real bytes flowed into a workspace's CC/terminal PTY (via `cc_input`). Carries
    /// the BYTE COUNT only — never the bytes. Attributed to the active workspace.
    KeystrokeActivity { byte_count: usize },
    /// The active right-panel surface changed (editor / diff / terminal). Lets WP3
    /// measure "editor-active + no keystrokes ⇒ reading code" vs terminal-following.
    ActiveSurface,
    /// Claudesk launched an external tool for a workspace (Sublime Text / Merge /
    /// Finder). The tool IDENTITY only — never the launched path. The signal a
    /// subsequent blur can be correlated against.
    ExternalLaunch { tool: NativeLaunchTool },
    /// A workspace's CC session was torn down by Claudesk — an explicit **session-end
    /// marker** (M9 WP6.5 signal 1). Written when Claudesk kills a PTY on workspace-close
    /// (`cc_kill`) or app-quit (`kill_all` on `CloseRequested`). This is the authoritative,
    /// synchronously-recorded end the reclassifier's [`crate::reclassify::authoritative_end`]
    /// reads at top precedence — the backstop for the hard-kill/crash case where CC cannot
    /// emit a `SessionEnd` (live-confirmed 2026-07-08). The row carries only the session
    /// handle + `now_ms()` (privacy: no content).
    WorkspaceClose,
}

/// Which Claudesk-initiated external tool a [`NativeSignal::ExternalLaunch`] marks.
/// A closed enum (not a free string) so the launch signal cannot carry a path.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum NativeLaunchTool {
    SublimeText,
    SublimeMerge,
    Finder,
}

impl NativeLaunchTool {
    /// The stable `tool` string persisted in `meta` (matches WP3's expected vocab).
    pub fn as_str(self) -> &'static str {
        match self {
            NativeLaunchTool::SublimeText => "sublime",
            NativeLaunchTool::SublimeMerge => "smerge",
            NativeLaunchTool::Finder => "finder",
        }
    }
}

/// The attribution context a native signal is stamped with — the active workspace
/// (from the frontend's `time_set_active_context`) plus, for keystroke rows, the
/// originating PTY `session_id`. All fields optional: a focus event may fire before
/// any workspace is active; a launch may target a workspace with no live session.
///
/// **Privacy:** `workspace_id` and `session_id` are opaque handles (`ws-…` / `cc-…`),
/// `cwd` is the project dir already stored on CC-hook rows — nothing content-bearing.
#[derive(Debug, Clone, Default, PartialEq, Eq)]
pub struct NativeContext {
    pub workspace_id: Option<String>,
    pub surface: Option<String>,
    pub cwd: Option<String>,
    pub session_id: Option<String>,
}

/// Map a [`NativeSignal`] + its attribution [`NativeContext`] to the [`TimeRow`] to
/// persist. Stamps `source = "claudesk-native"` and `ts = now_ms()` (native signals
/// carry their own send-time — see [`now_ms`]). The `event` column is the signal
/// kind; signal-specific extras + attribution go into `meta`.
///
/// **Privacy:** every value written to `meta` here is a count, an enum tag, a bool,
/// or an opaque handle — never keystroke content, never a file path beyond `cwd`.
/// Pinned by [`tests::native_row_never_carries_content`].
pub fn native_row(signal: &NativeSignal, ctx: &NativeContext) -> TimeRow {
    let mut meta = serde_json::Map::new();
    if let Some(ws) = &ctx.workspace_id {
        meta.insert("workspace_id".into(), json!(ws));
    }
    if let Some(surface) = &ctx.surface {
        meta.insert("surface".into(), json!(surface));
    }

    let event = match signal {
        NativeSignal::WindowFocus => "WindowFocus",
        NativeSignal::WindowBlur { preceded_by_launch } => {
            meta.insert("preceded_by_launch".into(), json!(preceded_by_launch));
            "WindowBlur"
        }
        NativeSignal::KeystrokeActivity { byte_count } => {
            // COUNT only — never the bytes (the privacy invariant).
            meta.insert("byte_count".into(), json!(byte_count));
            "KeystrokeActivity"
        }
        NativeSignal::ActiveSurface => "ActiveSurface",
        NativeSignal::ExternalLaunch { tool } => {
            meta.insert("tool".into(), json!(tool.as_str()));
            "ExternalLaunch"
        }
        // M9 WP6.5 signal 1 — must match `reclassify::EVENT_WORKSPACE_CLOSE`. No extra meta
        // beyond the attribution already inserted above; the session handle (in
        // `session_id`) + `ts` ARE the marker.
        NativeSignal::WorkspaceClose => "WorkspaceClose",
    };

    let meta = if meta.is_empty() {
        None
    } else {
        Some(serde_json::Value::Object(meta).to_string())
    };

    TimeRow {
        ts: now_ms(),
        // Keystroke rows carry the originating PTY session; other native signals are
        // window/workspace-level (empty session_id). The column is NOT NULL, so an
        // absent session is the empty string (matches the CC-hook rows' non-null cwd).
        session_id: ctx.session_id.clone().unwrap_or_default(),
        cwd: ctx.cwd.clone().unwrap_or_default(),
        event: event.to_string(),
        tool_name: None,
        agent_type: None,
        source: SOURCE_CLAUDESK_NATIVE.to_string(),
        meta,
    }
}

/// Schema DDL — idempotent (`IF NOT EXISTS`). Mirrors `claude-time`'s `events` table
/// plus the `source` discriminator column (M9 WP2.5-ready). Run by [`bootstrap`].
///
/// `WAL` + a busy-timeout are set on the *connection* in [`commands`], not here (they
/// are connection pragmas, not schema). The two indexes mirror `claude-time`'s
/// (session+ts for per-session scans, ts for day/range scans).
const SCHEMA_SQL: &str = "\
CREATE TABLE IF NOT EXISTS events (
  ts          INTEGER NOT NULL,
  session_id  TEXT NOT NULL,
  cwd         TEXT NOT NULL,
  event       TEXT NOT NULL,
  tool_name   TEXT,
  agent_type  TEXT,
  source      TEXT NOT NULL,
  meta        TEXT
);
CREATE INDEX IF NOT EXISTS idx_session_ts ON events(session_id, ts);
CREATE INDEX IF NOT EXISTS idx_ts ON events(ts);
";

/// One persisted time-analytics row, mapped from a [`HookEvent`]. Column-parallel to
/// the `events` table. `meta` is the serialized JSON blob (or `None` when the event
/// has no extras). Built by [`event_to_row`]; written by [`insert_row`].
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct TimeRow {
    pub ts: i64,
    pub session_id: String,
    pub cwd: String,
    pub event: String,
    pub tool_name: Option<String>,
    pub agent_type: Option<String>,
    /// Row source discriminator — [`SOURCE_CC_HOOK`] for every row this module writes.
    pub source: String,
    /// JSON blob of event-specific extras, or `None`. Never contains prompt text.
    pub meta: Option<String>,
}

/// Map a [`HookEvent`] to the [`TimeRow`] to persist, or `None` if the event carries
/// no `hook_event_name` (a malformed/empty event we never store).
///
/// The `meta` blob is assembled from the event's time-analytics fields — the SAME
/// keys `claude-time`'s hook emitted (`prompt_length_chars` / `tool_use_id` /
/// `source`) PLUS `notification_type` (the `Notification` type tag, WP3 needs it to
/// derive CC's AwaitingInput state) PLUS `reason` (the `SessionEnd` tag, M9 WP6.5) —
/// so the reclassifier reads the identical shape.
/// **Privacy: only `prompt_length_chars` (the LENGTH) is read; `HookEvent::prompt`
/// (the text) is never touched here.** `source = "cc-hook"` is stamped unconditionally.
///
/// `ts` uses the hook-side `timestamp` (epoch ms) when present, else 0 — the caller
/// (the drain thread) does not re-stamp; the hook's send-time is the event time.
pub fn event_to_row(event: &HookEvent) -> Option<TimeRow> {
    if event.hook_event_name.is_empty() {
        return None;
    }

    // Assemble meta from whichever extras this event carries. Mirrors claude-time's
    // per-event meta: UserPromptSubmit → {prompt_length_chars}; Pre/Post tool events →
    // {tool_use_id}; SessionStart → {source}; Notification → {notification_type}.
    // Multiple keys can co-exist harmlessly.
    let mut meta = serde_json::Map::new();
    if let Some(len) = event.prompt_length_chars {
        // LENGTH only — never the prompt text (the privacy invariant).
        meta.insert("prompt_length_chars".into(), json!(len));
    }
    if let Some(tuid) = &event.tool_use_id {
        meta.insert("tool_use_id".into(), json!(tuid));
    }
    if let Some(src) = &event.source {
        meta.insert("source".into(), json!(src));
    }
    if let Some(ntype) = &event.notification_type {
        // The Notification's type tag (`permission_prompt` / `elicitation_dialog` /
        // `idle_prompt` / …). The WRITER is meaning-agnostic — it just persists the tag;
        // the WP3 reclassifier decides which types mean "CC AwaitingInput" (reusing
        // `status_broadcaster`'s classifier). An enum tag, never content — same privacy
        // class as the keys above. Needed by WP3's B2/B4 human-state rule.
        meta.insert("notification_type".into(), json!(ntype));
    }
    if let Some(reason) = &event.reason {
        // The SessionEnd `reason` tag (`prompt_input_exit` / `other` / …). M9 WP6.5: the
        // session-end model honors SessionEnd as an authoritative end; the reason is
        // persisted for debugging (the derivation does not branch on it in v1). An enum-ish
        // tag, never content — same privacy class as `source`/`notification_type`.
        meta.insert("reason".into(), json!(reason));
    }
    let meta = if meta.is_empty() {
        None
    } else {
        Some(serde_json::Value::Object(meta).to_string())
    };

    Some(TimeRow {
        ts: event.timestamp.map(|t| t as i64).unwrap_or(0),
        session_id: event.session_id.clone(),
        cwd: event.cwd.clone(),
        event: event.hook_event_name.clone(),
        tool_name: event.tool_name.clone(),
        agent_type: event.agent_type.clone(),
        source: SOURCE_CC_HOOK.to_string(),
        meta,
    })
}

/// Create the `events` table + indexes if absent (idempotent). Called once when the
/// connection opens (in [`commands::TimeStore`]). `execute_batch` runs the multi-
/// statement DDL in one call.
pub fn bootstrap(conn: &Connection) -> rusqlite::Result<()> {
    conn.execute_batch(SCHEMA_SQL)
}

/// INSERT one [`TimeRow`]. Parameterized (no string interpolation — SQL-injection-
/// safe by construction, unlike claude-time's hand-quoted `sql_q`). The caller gates
/// on the tracking toggle BEFORE calling this ([`commands::write_gated`]).
pub fn insert_row(conn: &Connection, row: &TimeRow) -> rusqlite::Result<()> {
    conn.execute(
        "INSERT INTO events (ts, session_id, cwd, event, tool_name, agent_type, source, meta)
         VALUES (?1, ?2, ?3, ?4, ?5, ?6, ?7, ?8)",
        rusqlite::params![
            row.ts,
            row.session_id,
            row.cwd,
            row.event,
            row.tool_name,
            row.agent_type,
            row.source,
            row.meta,
        ],
    )?;
    Ok(())
}

